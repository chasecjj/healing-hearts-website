-- =====================================================
-- EXTEND link_purchases_on_signup FOR MULTI TYPE
-- =====================================================
-- The existing function handles enrollment and download types.
-- We extend it to iterate through multi-type grants.
-- =====================================================

CREATE OR REPLACE FUNCTION link_purchases_on_signup()
RETURNS TRIGGER AS $$
DECLARE
  contact_record RECORD;
  order_record RECORD;
  grant_record jsonb;
  err_msg text;
  linked_count integer := 0;
BEGIN
  SELECT * INTO contact_record
  FROM crm_contacts
  WHERE email = NEW.email;

  IF NOT FOUND THEN
    RAISE LOG 'link_purchases_on_signup: no CRM contact for %', NEW.email;
    RETURN NEW;
  END IF;

  UPDATE crm_contacts
  SET auth_user_id = NEW.id
  WHERE id = contact_record.id;

  FOR order_record IN
    SELECT o.id as order_id, o.stripe_payment_intent, p.access_grants
    FROM orders o
    JOIN products p ON p.slug = o.product_slug
    WHERE o.contact_id = contact_record.id
      AND o.status = 'completed'
  LOOP
    UPDATE orders SET auth_user_id = NEW.id WHERE id = order_record.order_id;
    linked_count := linked_count + 1;

    IF (order_record.access_grants->>'type') = 'enrollment' THEN
      INSERT INTO enrollments (user_id, course_id, status, stripe_payment_id)
      VALUES (
        NEW.id,
        (order_record.access_grants->>'course_id')::uuid,
        'active',
        order_record.stripe_payment_intent
      )
      ON CONFLICT (user_id, course_id) DO NOTHING;
    ELSIF (order_record.access_grants->>'type') = 'multi' THEN
      FOR grant_record IN SELECT * FROM jsonb_array_elements(order_record.access_grants->'grants')
      LOOP
        IF (grant_record->>'type') = 'enrollment' THEN
          INSERT INTO enrollments (user_id, course_id, status, stripe_payment_id)
          VALUES (
            NEW.id,
            (grant_record->>'course_id')::uuid,
            'active',
            order_record.stripe_payment_intent
          )
          ON CONFLICT (user_id, course_id) DO NOTHING;
        END IF;
        -- download type: no enrollment row needed
      END LOOP;
    END IF;
    -- Single 'download' type: no enrollment row needed
  END LOOP;

  RAISE LOG 'link_purchases_on_signup: linked % orders for user % (email %)',
    linked_count, NEW.id, NEW.email;

  RETURN NEW;
EXCEPTION WHEN OTHERS THEN
  GET STACKED DIAGNOSTICS err_msg = MESSAGE_TEXT;
  RAISE WARNING 'link_purchases_on_signup FAILED for user % (email %): %',
    NEW.id, NEW.email, err_msg;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

