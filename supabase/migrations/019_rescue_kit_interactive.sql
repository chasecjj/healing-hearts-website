-- =====================================================
-- Migration 019: Rescue Kit Interactive Portal Version
-- =====================================================
-- Adds 3 new tables for persistent user data:
--   - assessment_results (append-only history)
--   - form_responses (upsert per user/lesson/form)
--   - practice_plan_progress (upsert per user/lesson/plan)
--
-- Adds the 'multi' access_grants type (semantic only — jsonb schema unchanged).
-- Creates the Rescue Kit course with 3 modules and 14 lessons (empty content_json).
-- Updates the existing rescue-kit product to use multi access_grants.
-- Extends link_purchases_on_signup() to handle the multi type.
--
-- IMPORTANT: Content for the 14 lessons is seeded separately via UPDATE
-- statements generated by `course-lab/tools/md_to_blocks.py`. This migration
-- creates the lesson ROWS but leaves content_json as empty arrays.
-- =====================================================

-- =====================================================
-- NEW TABLES
-- =====================================================

CREATE TABLE assessment_results (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id uuid NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  lesson_id uuid NOT NULL REFERENCES lessons(id) ON DELETE CASCADE,
  assessment_id text NOT NULL,
  responses jsonb NOT NULL,
  total_score integer NOT NULL,
  result_zone text NOT NULL,
  created_at timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX idx_assessment_results_user_lesson_created
  ON assessment_results(user_id, lesson_id, created_at DESC);

CREATE TABLE form_responses (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id uuid NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  lesson_id uuid NOT NULL REFERENCES lessons(id) ON DELETE CASCADE,
  form_id text NOT NULL,
  fields jsonb NOT NULL DEFAULT '{}'::jsonb,
  updated_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT form_responses_user_lesson_form_unique UNIQUE (user_id, lesson_id, form_id)
);

CREATE TABLE practice_plan_progress (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id uuid NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  lesson_id uuid NOT NULL REFERENCES lessons(id) ON DELETE CASCADE,
  plan_id text NOT NULL,
  days_completed jsonb NOT NULL DEFAULT '[]'::jsonb,
  notes_per_day jsonb NOT NULL DEFAULT '{}'::jsonb,
  updated_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT practice_plan_progress_user_lesson_plan_unique UNIQUE (user_id, lesson_id, plan_id)
);

